     public void Reporte5() throws SQLException,JRException {
        Connection conn;
     
      int x=Integer.parseInt(cODIGO.getText());
       
    
      conn=DriverManager.getConnection("jdbc:sqlserver://localhost:49162;databaseName=PCEPC","sa","12");
      JasperReport report = JasperCompileManager.compileReport("C:\\Proyecto\\build\\classes\\Reporte\\listadomodulos.jrxml");
      Map parametro = new HashMap();
      parametro.put("ID" ,x);
   
      
      
      JasperPrint print = JasperFillManager.fillReport(report, parametro, conn);  
      
      
     
      JasperViewer jw = new JasperViewer(print,false);
      jw.setTitle("CURSOS");
      jw.setVisible(true);
      
    }
