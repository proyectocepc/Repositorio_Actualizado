    private void ReusuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ReusuarioKeyTyped
        char b = evt.getKeyChar();
        if (Reusuario.getText().length() >= 10) {
            evt.consume();
        }
        if ((b < 'a' || b > 'z') && (b < '0' || b > '9') && (b < 'A' || b > 'Z')&& (b != 'ñ') && (b != 'Ñ')) {
            evt.consume();
        }
    }//GEN-LAST:event_ReusuarioKeyTyped

