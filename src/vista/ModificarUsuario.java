    private void GuardarCambiosUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarCambiosUsuActionPerformed
        try{
            PreparedStatement pps = Conexiones.Conexion.getConexion().prepareStatement("update USUARIO set USUARIO='" +
                Reusuario.getText() + "', CONTRASEÑA='" + Recontraseña.getText() + "', TIPO_USU='"
                + Cargo.getSelectedItem() + "', PREGUNTA_SECRETA='"
                + PDseguridad.getText() + "', RESPUESTA_sEC='" + Respuestas.getText() +
                "' where CI='" + Ciusuario.getText() + "'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "LOS DATOS HAN SIDO MODIFICADOS");

            Reusuario.setText("");
            Recontraseña.setText("");
            PDseguridad.setText("");
            Respuestas.setText("");
            Cargo.setSelectedItem("");

            Reusuario.requestFocus();
            Recontraseña.requestFocus();
            PDseguridad.requestFocus();
            Respuestas.requestFocus();
            Cargo.requestFocus();
            Ciusuario.requestFocus();

        }catch(SQLException e){
        }
        Ciusuario.setText("");
        Reusuario.setEnabled(false);
        Recontraseña.setEnabled(false);
        PDseguridad.setEnabled(false);
        Respuestas.setEnabled(false);
        Cargo.setEnabled(false);
        this.GuardarCambiosUsu.setVisible(false);
        this.BuscarUsu.setVisible(false);
    }//GEN-LAST:event_GuardarCambiosUsuActionPerformed

    private void BuscarUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarUsuActionPerformed
        if(Ciusuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Coloque Bien sus Datos","Error",JOptionPane.ERROR_MESSAGE);
            Ciusuario.setText("");
            Ciusuario.requestFocus();
        }else{
            try{
                String b;
                BDConexion.BuscarUsuario(Integer.parseInt(Ciusuario.getText()));
                b=Ciusuario.getText();
                Reusuario.setText("");
                Recontraseña.setText("");
                Cargo.setSelectedItem("");
                PDseguridad.setText("");
                Respuestas.setText("");

                Ciusuario.setText("");

                Reusuario.requestFocus();
                Recontraseña.requestFocus();
                PDseguridad.requestFocus();
                Respuestas.requestFocus();
                Cargo.requestFocus();
                Ciusuario.requestFocus();
                res= Conexiones.Conexion.Consulta("select * from USUARIO");
                while(res.next()){
                    if(res.getString(6).equals(b)){
                        JOptionPane.showMessageDialog(null,"Datos Encontrados");
                        Reusuario.setText(res.getString(1));
                        Recontraseña.setText(res.getString(2));
                        Cargo.setSelectedItem(res.getString(3));
                        PDseguridad.setText(res.getString(4));

                        Respuestas.setText(res.getString(5));

                        Ciusuario.setText(res.getString(6));

                    }else{
                        System.out.println("datos encontrados");
                    }

                }
                Ciusuario.setEditable(false);
                Reusuario.setEnabled(true);
                Recontraseña.setEnabled(true);
                PDseguridad.setEnabled(true);
                Respuestas.setEnabled(true);
                Cargo.setEnabled(true);
                  

                this.GuardarCambiosUsu.setVisible(true);
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_BuscarUsuActionPerformed

