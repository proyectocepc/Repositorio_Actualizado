    public void Validacionadm() {
        String mensaje = BDConexion.bucarCedulaE(Cedulaadm.getText());
        if (mensaje.equals("La Cedula ya Existe")) {
            CodExisteadm.setText("Cedula empleado");
            ModificarAdm2.setEnabled(true);
            EspecialidadAdm2.setEnabled(true);
            if (Cedulaadm.getText().length() == 8 && !EspecialidadAdm2.getText().isEmpty()) {
            saveadm.setEnabled(true);
            }
        } else if (Cedulaadm.getText().isEmpty()) {
            CodExisteadm.setText("");

        } else {
            CodExisteadm.setText("Cedula no Registrada");
            ModificarAdm2.setEnabled(false);
            EspecialidadAdm2.setEnabled(false);
            saveadm.setEnabled(false);
        }
    }
    public void GuardarAdmin(){
         if (Cedulaadm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "rellene todos los campos obligatorios", "informacion", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                String Ced, Espec;
                BDConexion.EntradaAdministrativo(
                   
                    Espec = EspecialidadAdm2.getText(),
                         Ced = Cedulaadm.getText()
                );
                EspecialidadAdm2.setText("");

                JOptionPane.showMessageDialog(null, "Guardado Exitoso");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

        }

        EspecialidadAdm2.setEnabled(false);
        Texto2 = Cedulaadm.getText();
    }
    public void GuardarCambios(){
         try {
            PreparedStatement pps = Conexiones.Conexion.getConexion().prepareStatement("update ADMINISTRATIVO set ESPECIALIDAD='"
                + EspecialidadAdm2.getText()
                + "' where CI='" + Cedulaadm.getText() + "'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "LOS DATOS HAN SIDO MODIFICADOS");

            EspecialidadAdm2.setText("");
            EspecialidadAdm2.requestFocus();
            DatosAdm.setEnabled(false);

        } catch (SQLException e) {
        }
        Cedulaadm.setText("");
        EspecialidadAdm2.setEnabled(false);
        this.Cambiosadm.setEnabled(false);
        saveadm.setEnabled(true);
    }
    public void ModificarAdmin(){
         if (Cedulaadm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Coloque Bien sus Datos", "Error", JOptionPane.ERROR_MESSAGE);
            Cedulaadm.setText("");
            Cedulaadm.requestFocus();
        } else {
            try {
                String b;
                BDConexion.BuscarAdm(Integer.parseInt(Cedulaadm.getText()));
                b = Cedulaadm.getText();

                Cedulaadm.requestFocus();
                EspecialidadAdm2.requestFocus();

                res = Conexiones.Conexion.Consulta("select ESPECIALIDAD,CI FROM ADMINISTRATIVO");
                while (res.next()) {
                    if (res.getString(1).equals(b)) {
                        JOptionPane.showMessageDialog(null, "Datos Encontrados");
                        Cedulaadm.setText(res.getString(1));
                        EspecialidadAdm2.setText(res.getString(2));

                    }

                }
                Cedulaadm.setEditable(false);
                DatosAdm.setEnabled(false);

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        EspecialidadAdm2.setEnabled(true);
        this.Cambiosadm.setVisible(true);
        saveadm.setVisible(false);
    }

    
