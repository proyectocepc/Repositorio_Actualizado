    public void CargarDatosUsuario() {
        DefaultTableModel modelo = (DefaultTableModel) UsuariosRegistrados.getModel();
        modelo.setRowCount(0);
        res = Conexiones.Conexion.Consulta("SELECT USUARIO,TIPO_USU,CI FROM USUARIO");
        try {
            while (res.next()) {
                Vector v = new Vector();
                v.add(res.getString(1));
                v.add(res.getString(2));
                v.add(res.getInt(3));

                modelo.addRow(v);
                UsuariosRegistrados.setModel(modelo);
            }
        } catch (SQLException e) {
        }
    }

