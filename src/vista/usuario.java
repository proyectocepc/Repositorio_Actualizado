    private void GuardarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarUsuarioActionPerformed
        if (Ciusuario.getText().isEmpty() || Reusuario.getText().isEmpty() || Recontraseña.getText().isEmpty() || Respuestas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "rellene todos los campos obligatorios", "informacion", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                String Cius, Usu, Con, Psegu, Rsegu, Carg;
                BDConexion.EntradaUsuario(
                    Usu = Reusuario.getText(),
                    Con = Recontraseña.getText(),
                    Carg = Cargo.getSelectedItem().toString(),
                    Psegu = PDseguridad.getText(),
                    Rsegu = Respuestas.getText(),
                    Cius = Ciusuario.getText());
                PDseguridad.setText("");
                Respuestas.setText("");
                Reusuario.setText("");
                Recontraseña.setText("");
                JOptionPane.showMessageDialog(null, "Guardado Exitoso");
          Respuestas.setEnabled(false);
             Reusuario.setEnabled(false);
          Recontraseña.setEnabled(false);
            PDseguridad.setEnabled(false);
            Cargo.setEnabled(false);   
            GuardarUsuario.setEnabled(false);   
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

        }
    }//GEN-LAST:event_GuardarUsuarioActionPerformed

