public void HabilitarBoton(){
        if(CodigoCurso.getText().length()==4&&!modulo.getText().isEmpty()){
            SaveModulo.setEnabled(true);
        }else{
            SaveModulo.setEnabled(false);
        }
    }
public void  CargarDatos(){
        DefaultTableModel modelo =(DefaultTableModel) Tmodulo.getModel();
        modelo.setRowCount(0);
        res = Conexiones.Conexion.Consulta("SELECT * FROM MODULO");
        try{
            while(res.next()){
                Vector v = new Vector();
            v.add(res.getInt(1));
            v.add(res.getInt(2));
            v.add(res.getString(3));
            modelo.addRow(v);
            Tmodulo.setModel(modelo);
            }
        }catch (SQLException e){            
        }
}
        public void  FiltrarDatos(String Valor){
        DefaultTableModel modelo =(DefaultTableModel) Tmodulo.getModel();
        modelo.setRowCount(0);
        res = Conexiones.Conexion.Consulta("SELECT * FROM MODULO WHERE MODULO.ID_CURSO LIKE'%"+Valor+"%'");
        try{
            while(res.next()){
                Vector v = new Vector();
            v.add(res.getInt(1));
            v.add(res.getInt(2));
            v.add(res.getString(3));
            modelo.addRow(v);
            Tmodulo.setModel(modelo);
            }
        }catch (SQLException e){            
        }
    }
        public void GuardarModulo(){
            if (CodigoCurso.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this,"rellene todos los campos obligatorios",   "informacion", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                String cod,mod;
                BDConexion.EntradaModulo(

                    cod=CodigoCurso.getText(),
                    mod=modulo.getText());
                JOptionPane.showMessageDialog(null, "Guardado Exitoso");
                modulo.setText("");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,  ex);
            }

        }
        }
        public void CambiosModulo(){
            try {
            PreparedStatement pps = Conexiones.Conexion.getConexion().prepareStatement("update MODULO set MOD_CURS='" + modulo.getText() +
                "' where ID_CURSO='" + CodigoCurso.getText() + "' AND ID_MODULOS= '" + id.getText() + "'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "LOS DATOS HAN SIDO MODIFICADOS");

            modulo.setText("");

            modulo.requestFocus();

        } catch (SQLException e) {
        }
        }

