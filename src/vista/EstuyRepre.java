    public void  CargarDatos(){
        DefaultTableModel modelo =(DefaultTableModel) Tmodulo.getModel();
        modelo.setRowCount(0);
        res = Conexiones.Conexion.Consulta(" SELECT P_NOMBRE,P_APELLIDO,PREF,NUMERO,PARENTESCO,ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR" +
" ,ESTUDIANTE.CI FROM PERSONA  JOIN ESTUDIANTE_PUEDE_TENER_REPRESENTANTE" +
" ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR=PERSONA.CI JOIN TELEFONO ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR=TELEFONO.CI JOIN ESTUDIANTE" +
" ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIE=ESTUDIANTE.CI  ");
        try{
            while(res.next()){
                Vector v = new Vector();
            v.add(res.getString(1));
            v.add(res.getString(2));
            v.add(res.getInt(3));
            v.add(res.getInt(4));
            v.add(res.getString(5));
            v.add(res.getInt(6));
            v.add(res.getInt(7));
            modelo.addRow(v);
            Tmodulo.setModel(modelo);
            }
        }catch (SQLException e){            
        }
}
        public void  FiltrarDatos(String Valor){
        DefaultTableModel modelo =(DefaultTableModel) Tmodulo.getModel();
        modelo.setRowCount(0);
        res = Conexiones.Conexion.Consulta(" SELECT P_NOMBRE,P_APELLIDO,PREF,NUMERO,PARENTESCO,ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR" +
" ,ESTUDIANTE.CI FROM PERSONA  JOIN ESTUDIANTE_PUEDE_TENER_REPRESENTANTE" +
" ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR=PERSONA.CI JOIN TELEFONO ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR=TELEFONO.CI JOIN ESTUDIANTE" +
" ON ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIE=ESTUDIANTE.CI  WHERE ESTUDIANTE_PUEDE_TENER_REPRESENTANTE.CIR  LIKE'%"+Valor+"%'");
        try{
            while(res.next()){
                Vector v = new Vector();
             v.add(res.getString(1));
            v.add(res.getString(2));
            v.add(res.getInt(3));
            v.add(res.getInt(4));
            v.add(res.getString(5));
            v.add(res.getInt(6));
            v.add(res.getInt(7));
            modelo.addRow(v);
            Tmodulo.setModel(modelo);
            }
        }catch (SQLException e){            
        }
    }

